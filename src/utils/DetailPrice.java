public class DetailPrice {
    private Integer leftSeat;
    private String rightToChange;
    private String cancellationRight;
    private String moneyRefund;
    private String baggageRight;
    private String handBaggage;

    public Integer getLeftSeat() {
        return leftSeat;
    }

    public void setLeftSeat(Integer leftSeat) {
        this.leftSeat = leftSeat;
    }

    public String getRightToChange() {
        return rightToChange;
    }

    public void setRightToChange(String rightToChange) {
        this.rightToChange = rightToChange;
    }

    public String getCancellationRight() {
        return cancellationRight;
    }

    public void setCancellationRight(String cancellationRight) {
        this.cancellationRight = cancellationRight;
    }

    public String getMoneyRefund() {
        return moneyRefund;
    }

    public void setMoneyRefund(String moneyRefund) {
        this.moneyRefund = moneyRefund;
    }

    public String getBaggageRight() {
        return baggageRight;
    }

    public void setBaggageRight(String baggageRight) {
        this.baggageRight = baggageRight;
    }

    public String getHandBaggage() {
        return handBaggage;
    }

    public void setHandBaggage(String handBaggage) {
        this.handBaggage = handBaggage;
    }
    
    
}
